// Write a Java program to print the result of the given arithmetic operations.
public class ArithmeticExpressions {
    public static void main(String[] args) {
        System.out.println("a. " + (-5 + 8 * 6));
        System.out.println("b. " + ((-5 + 8) * 6));
        System.out.println("c. " + ((55 + 9) % 9));
        System.out.println("d. " + (20 + -3 * 5 / 8));
        System.out.println("e. " + (5 + 15 / 3 * 2 - 8 % 3));
    }
}